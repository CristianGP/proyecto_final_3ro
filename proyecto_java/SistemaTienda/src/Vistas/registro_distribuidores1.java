    public void llenarDatos() {
        try {
                this.jtf_id.setText(rs.getString("DISTRIBUIDORID"));
                this.jtf_nombre.setText(rs.getString("NOMBRED"));
                this.jtf_telefono.setText(rs.getString("TELEFONO"));
                this.jtf_direccion.setText(rs.getString("DIRECCION"));
                this.jtf_estado.setText(rs.getString("ESTADO"));
                this.jtf_ciudad.setText(rs.getString("CIUDAD"));
              
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error 001" + ex.getMessage());
        }
    }
    
    
    
