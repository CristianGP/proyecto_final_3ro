    private void btn_guardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_guardarMouseClicked

        try{

            ps = con.prepareStatement("INSERT INTO distribuidor (NOMBRED, TELEFONO, DIRECCION, ESTADO, CIUDAD) VALUES(?,?,?,?,?) ");
            ps.setString(1, jtf_nombre.getText());
            ps.setString(2, jtf_telefono.getText());
            ps.setString(3, jtf_direccion.getText());
            ps.setString(4, jtf_estado.getText());
            ps.setString(5, jtf_ciudad.getText());

            int res = ps.executeUpdate();

            if(res > 0){
                JOptionPane.showMessageDialog(null, "Registro Guardado");

            } else {
                JOptionPane.showMessageDialog(null, "Error al Guardar");

            }

            

        } catch(Exception e){
            System.err.println(e);
        }

    }//GEN-LAST:event_btn_guardarMouseClicked

    private void jbt_modificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jbt_modificarMouseClicked

        try{

            ps = con.prepareStatement("UPDATE distribuidor  SET NOMBRED=?, TELEFONO=?, DIRECCION=?, ESTADO=?, CIUDAD=? WHERE DISTRIBUIDORID=?");
            ps.setString(1, jtf_nombre.getText());
            ps.setString(2, jtf_telefono.getText());
            ps.setString(3, jtf_direccion.getText());
            ps.setString(4, jtf_estado.getText());
            ps.setString(5, jtf_ciudad.getText());
            ps.setString(6, jtf_id.getText());

            int res = ps.executeUpdate();

            if(res > 0){
                JOptionPane.showMessageDialog(null, "Producto Modificado");
                limpiarCajas();

            } else {
                JOptionPane.showMessageDialog(null, "Error al Modificar ");

            }

           

        } catch(Exception e){
            System.err.println(e);
        }

    }//GEN-LAST:event_jbt_modificarMouseClicked

