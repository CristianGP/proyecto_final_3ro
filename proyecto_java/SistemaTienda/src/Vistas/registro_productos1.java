        public void Conectar() {
            btn_guardar.setVisible(false);
            btn_buscar.setVisible(false);
        try {
            st = con.createStatement();
            rs = st.executeQuery("Select * from PRODUCTO;");
            rs.next();
            llenarDatos();

        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null, "Error 000" + err.getMessage());
        }

    }

    public void llenarDatos() {
        try {
                this.jtf_id.setText(rs.getString("PRODUCTOID"));
                this.jtf_clave.setText(rs.getString("CLAVE"));
                this.jtf_nombre.setText(rs.getString("NOMBREP"));
                this.jtf_contenido.setText(rs.getString("CONTENIDO"));
                this.cbx_unidades.setSelectedItem(rs.getString("UNIDADES"));
                this.jtf_venta.setText(rs.getString("PRECIOVENTA"));
                this.jtf_stok.setText(rs.getString("STOKACTUAL"));
            
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error 001" + ex.getMessage());
        }
    }
    
    
    
