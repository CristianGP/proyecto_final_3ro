    private void btn_guardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_guardarMouseClicked
        
        
        try{
            
            ps = con.prepareStatement("INSERT INTO producto (CLAVE, NOMBREP, CONTENIDO, UNIDADES, PRECIOVENTA, STOKACTUAL) VALUES(?,?,?,?,?,?) ");
            ps.setString(1, jtf_clave.getText());
            ps.setString(2, jtf_nombre.getText());
            ps.setString(3, jtf_contenido.getText());
            ps.setString(4, cbx_unidades.getSelectedItem().toString());
            ps.setString(5, jtf_venta.getText());
            ps.setString(6, jtf_stok.getText());
            
            
            int res = ps.executeUpdate();
            
            if(res > 0){
                JOptionPane.showMessageDialog(null, "Registro Guardado");
               
  
            } else {
                 JOptionPane.showMessageDialog(null, "Error al Guardar");
                 
            }
            
            
            
            
        } catch(Exception e){
            System.err.println(e);
        }
     
                 
    }//GEN-LAST:event_btn_guardarMouseClicked

    private void btn_buscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_buscarMouseClicked
        jtb_eliminar.setVisible(true);
        btn_guardar.setVisible(false);
        try{
            
          
            ps = con.prepareStatement("SELECT * FROM producto WHERE CLAVE = ?");
            ps.setString(1, jtf_clave.getText());
            
            rs = ps.executeQuery();
            
            if(rs.next()){
                jtf_id.setText(rs.getString("PRODUCTOID"));
                jtf_nombre.setText(rs.getString("NOMBREP"));
                jtf_contenido.setText(rs.getString("CONTENIDO"));
                cbx_unidades.setSelectedItem(rs.getString("UNIDADES"));
                jtf_venta.setText(rs.getString("PRECIOVENTA"));
                jtf_stok.setText(rs.getString("STOKACTUAL"));
                
            } else {
                JOptionPane.showMessageDialog(null, "No existe producto con esta clave");
            }
            
        } catch(Exception e){
            System.err.println(e);
        }
    }//GEN-LAST:event_btn_buscarMouseClicked

